    <p>
      <b>Sujet:</b> Une bureau d'audit et de conseil souhaite analyser et explorer des données. 67 entreprises ont répondues à un questionnaire ayant pour but 
      de connaître le niveau d'expertise des différentes entreprises par rapport à un concept. Les réponses à ce questionnaire sont limitées (débutant,
      intermédiaire, confirmé, expert, pas interresée). Les questions sont catégorisées et plusieurs informations sur les entreprises sont recueillis (taille, région,
      secteur d'activités...).
      <br/>
      À travers cette application web, nous allons donc vous présenter plusieurs représentation de données. Les onglets placés en haut à droite sont dans l'ordre des tâches visuelles demandées.
