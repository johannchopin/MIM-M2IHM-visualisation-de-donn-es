      <b>Le projet:</b> L'objectif est de mettre en application les enseignements issus du cours de Visualisation de données.<br/> Lorsqu'on est amené 
      à travailler avec des données, il est important de penser à la représentation adéquate de celles-ci. En effet, les données ont besoin d'être modéliser
      afin d'être analyser, une représentation différentes des données peut en changer l'interprétation. C'est dans cette philosophie que nous avons décidé
      de la manière la plus efficace et correcte de représenter les données selon l'interprétation souhaitée.
